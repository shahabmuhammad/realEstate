    <script>
      let slideIndex_ = 1;
      showSlides_(slideIndex_);

      function plusSlides_(n) {
        showSlides_((slideIndex_ += n));
      }

      function currentSlide_(n) {
        showSlides_((slideIndex_ = n));
      }

      function showSlides_(n) {
        let i;
        let slides_ = document.getElementsByClassName("mySlides_");
        let dots_ = document.getElementsByClassName("demo_");
        let captionText_ = document.getElementById("caption_");
        if (n > slides_.length) {
          slideIndex_ = 1;
        }
        if (n < 1) {
          slideIndex_ = slides_.length;
        }
        for (i = 0; i < slides_.length; i++) {
          slides_[i].style.display = "none";
        }
        for (i = 0; i < dots_.length; i++) {
          dots_[i].className = dots_[i].className.replace(" active_", "");
        }
        slides_[slideIndex_ - 1].style.display = "block";
        dots_[slideIndex_ - 1].className += " active_";
        captionText_.innerHTML = dots_[slideIndex_ - 1].alt;
      }

      const slider = document.querySelector(".slider__");
      const prev = document.querySelector(".prev__");
      const next = document.querySelector(".next__");

      let currentIndex = 0;
      const slidesToShow = 3; // Number of visible slides
      const totalSlides = document.querySelectorAll(".slide__").length;

      // Calculate slide width
      const slideWidth = document.querySelector(".slide__").offsetWidth + 20; // 20px is the gap

      // Function to update slider position
      function updateSlider() {
        slider.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
      }

      // Event listeners for navigation buttons
      prev.addEventListener("click", () => {
        if (currentIndex > 0) {
          currentIndex--;
          updateSlider();
        }
      });

      next.addEventListener("click", () => {
        if (currentIndex < totalSlides - slidesToShow) {
          currentIndex++;
          updateSlider();
        }
      });
    </script>